-module(rabbit_tracer).
-export([start/0]).

-import(erlang).

start() ->
    spawn(fun mainloop/0),
    ok.

mainloop() ->
    erlang:trace(new, true, [all]),
    mainloop1().

mainloop1() ->
    receive
        Msg ->
            rabbit_log:info("TRACE: ~p~n", [Msg])
    end,
    mainloop1().
